package org.example;

import java.math.BigInteger;

public class Primefactor {
    private int p;
    private int q;

    public Primefactor(int p, int q) {
        this.p = p;
        this.q = q;
    }
    public int getP() {
        return this.p;
    }
    public int getQ() {
        return this.q;
    }
}
