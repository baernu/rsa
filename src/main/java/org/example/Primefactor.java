package org.example;

import java.math.BigInteger;

public class Primefactor {
    private long p;
    private long q;

    public Primefactor(long p, long q) {
        this.p = p;
        this.q = q;
    }
    public long getP() {
        return this.p;
    }
    public long getQ() {
        return this.q;
    }
}
